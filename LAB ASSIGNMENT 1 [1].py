#QUESTION

# How to accept data from user in the program at run time? Explain with the help of example.

#SOLUTION

#In Python data can directly be printed using print function.
#For Example:

print("I Love Python")

#Data can be assigned in python using variables.
#For Example:

my_name = ("ANAND ANIL DAREKAR")
print("My name is", my_name)

#The python also comes with an Input Function, where we have to enter our answer(INPUT).
#For Example:

first_name = input("Enter your First Name: ")
middle_name = input("Enter your Middle Name: ")
last_name = input("Enter your Last Name: ")

print("Your Name is", first_name, middle_name, last_name)

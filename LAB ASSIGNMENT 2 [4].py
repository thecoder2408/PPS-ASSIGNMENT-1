#Explain execution of while loop with example.

#SOLUTION:
'''
Python While Loop is used to execute a block of statements repeatedly until a given condition is satisfied. 
And when the condition becomes false, the line immediately after the loop in the program is executed.

SYNTAX:
while expression:
    statement(s)

When we insert a while loop inside a programm, It functions differently. While loop is executes until the loop is completed.
we have to give a input value and give a condition for while loop to execute.
'''

#For Example:
count = 0
while (count < 10):
    count = count + 1
    print("I LOVE PPS")
#Here (I LOVE PPS) will be printed till value of count doesn't cross 10.

#This is how a while loops work.
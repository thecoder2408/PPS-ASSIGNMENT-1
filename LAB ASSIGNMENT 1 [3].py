#QUESTION
#Which are the different comparison operators used in python?

#SOLUTION
'''  

There are 6 types of comparison operators:
1: ( == )       ->          [ EQUAL TO ]
2: ( != )       ->          [ NOT EQUAL TO ]
3: ( < )        ->          [ LESS THAN ]  
4: ( > )        ->          [ GREATER THAN ]
5: ( >= )       ->          [ GREATER THAN OR EQUAL TO ]
6: ( <= )       ->          [ LESS THAN OR EQUAL TO ] 

'''

  


